//Esta es otra forma para agregar:

const mesesDelAño = [
    "Enero",
    "Febrero",
    "Marzo",
    "Abril",
    "Mayo",
    "Junio",
    "Julio",
    "Agosto",
    "Septiembre",
    "Octubre",
    "Noviembre",
    "Diciembre",
  ],
  $ul3 = document.createElement("ul");

$fragment = document.createDocumentFragment(); //Con esto genera fragmentos de los elementos

mesesDelAño.forEach((el) => {
  const $li = document.createElement("li"); //Se agrega el li al ul
  $li.textContent = el; //Se agrega el contenido de texto
  $fragment.appendChild($li); //Se agrega cada fragmento.
});

document.write(`<h3>Meses del Año</h3>`); //Solo para el ejercicio.

$ul3.appendChild($fragment); //Al $ul3 se le agrega los fragmnentos de $fragment

document.body.appendChild($ul3); //Aquí ya se incluye el $ul3 al body.

