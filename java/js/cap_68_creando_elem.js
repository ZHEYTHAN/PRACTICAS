//Ahora aquí viene una forma para poder agregar formas de manera dinámica al DOM ----

const estaciones = ["Primavera", "Verano", "Otoño", "Invierno"],
  $ul = document.createElement("ul"); // Creación del ul para las listas.
//
//
//
// document.write() solo se usará para este ejemplo ---  ya que no es recomendable ni está en el estandar para usar.

document.write(`<h3> Estaciones del Año </h3>`);

//Aquí se agregará la ul en el body.
document.body.appendChild($ul);

//Ahora en el forEach se hará la adición de los li

estaciones.forEach((el) => {
  const $li = document.createElement("li"); //Aquí el forEach por cada elemento crea un li
  $li.textContent = el; // Aquí a cada li le agrega el texto de cada lista. (Por unidad)
  $ul.appendChild($li); //Aquí agrega cada li ya modificado dentro del $ul
});

