//Eliminaré una de las tarjetas que ya existen (No de las any que se acaban de agregar).
//
//

//Aquí eliminó la última tarjeta
$theCards.removeChild($theCards.lastElementChild);

//Aquí se elimino la tarjeta número 7 (Como se cuenta desde 0 las posiciones --- entonces sería el puesto 6)
