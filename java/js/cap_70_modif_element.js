//En este se realizara el uso de otro metodo para insertar, reemplazar, eliminar cosas
//Son metodos que se usan desde hace un tiempo --- Se asemejan al uso de JQuery.
//Este ejercicio se hará sobre la section (class= "cards") en donde dentro se encuentran las tarjetas.

($theCards = document.querySelector(".cards")), //variable para relacionar la clase de la section de las tarjetas.
  ($newCard = document.createElement("figure")); //La nueva variable que se usara para los metodos

$newCard.innerHTML = `
<img src="https://placeimg.com/200/200/any" alt="Any">
<figcaption> Any </figcaption>
`; // Este innerHTML es para crear las etiquetas para la tarjeta que se usará de prueba.

$newCard.classList.add("card"); //Adición de la clase card a la nueva tarjeta(A la variable newCard)
//
//
/*
A partir de aquí se usarán otros metodos diferentes a .appendChild()
*/

//Metodo para reemplazar --- Se reemplazará una tarjeta:

//
//La propiedad replace requiere de dos valores en el parentesis -- el primer valor es el valor nuevo -- el segundo valor va el padre.children[La posición del hijo que se va a reemplazar]

//

$theCards.replaceChild($newCard, $theCards.children[2]);
//

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//Creare una segunda variable para uso de otra propiedad:
//
//
($theCards2 = document.querySelector(".cards")),
  ($newCard2 = document.createElement("figure")),
  $newCard2.classList.add("card");

$newCard2.innerHTML = `
<img src="https://placeimg.com/200/200/any" alt="Any2">
<figcaption> Any segunda propiedad </figcaption>
`;

//La siguiente propiedad la inserta, ya sea antes de la primera tarjeta, o antes de cualquier tarjeta que se declare:
//Esta propiedad de insertBefore requiere de dos valores, el primer valor es el valor a agregar y el segundo es la posición en donde va a ir:

$theCards2.insertBefore($newCard2, $theCards2.firstElementChild);
//
// $theCards2.insertBefore($newCard2, $theCards2.children[4]); //Segunda forma con posicionamiento (coordenada).

