//Se verán las propiedades básicas Del BOM: Browser Object Model

//Los siguientes son para validar información de ancho y alto del navegador:

console.log(window.innerWidth); //Muestra el ancho que toma la parte visual de la página (Sin contar si se abre la consola o la barra de arriba de marcadores)
console.log(window.innerHeight); //Muestra el alto que toma la parte visual de la página (Sin contar si se abre la consola o la barra de arriba de marcadores)
console.log(window.outerWidth); //Muestra el ancho que toma la parte visual de la página (Todo el tamaño que cubre el navegador)
console.log(window.outerHeight); //Muestra el alto que toma la parte visual de la página (Todo el tamaño que cubre el navegador)

//Ahora se hará en un evento:
//resize: propiedad que recalcula los valores dentro de los console.log

window.addEventListener("resize", (e) => {
  console.clear();

  console.log(
    `****************************** Evento Resize ***************************`
  );

  console.log(window.innerWidth); //Muestra el ancho que toma la parte visual de la página (Sin contar si se abre la consola o la barra de arriba de marcadores)
  console.log(window.innerHeight); //Muestra el alto que toma la parte visual de la página (Sin contar si se abre la consola o la barra de arriba de marcadores)
  console.log(window.outerWidth); //Muestra el ancho que toma la parte visual de la página (Todo el tamaño que cubre el navegador)
  console.log(window.outerHeight); //Muestra el alto que toma la parte visual de la página (Todo el tamaño que cubre el navegador)

  console.log(e);
});

