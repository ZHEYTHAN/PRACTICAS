//Ahora también hay una propiedad para los scroll:

// Estos se miden de eje X y del eje Y.
//El eje X se mide desde el punto cero de la izquierda (El borde de la izquiera es el punto cero de x).
//El eje Y se mide desde el top  (El margen top de la pantalla es el punto cero de Y).

console.log(window.scrollY);
console.log(window.scrollX);

window.addEventListener("scroll", (e) => {
  console.clear();

  console.log(
    `****************************** Evento Scroll ***************************`
  );

  console.log(window.scrollY);
  console.log(window.scrollX);

  console.log(e);
});

